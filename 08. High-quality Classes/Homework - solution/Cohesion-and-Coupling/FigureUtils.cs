using System;

namespace CohesionAndCoupling
{
    public class FigureUtils
    {
        private double width;
        private double height;
        private double depth;

        public FigureUtils(double width, double height)
        {
            this.Width = width;
            this.Height = height;
            this.Depth = 0;
        }

        public FigureUtils(double width, double height, double depth)
        {
            this.Width = width;
            this.Height = height;
            this.Depth = depth;
        }

        public double Width
        {
            get
            {
                return this.width;
            }

            private set
            {
                if (value < 0)
                {
                    throw new ArgumentOutOfRangeException("Invalid width");
                }

                this.width = value;
            }
        }

        public double Height
        {
            get
            {
                return this.height;
            }

            private set
            {
                if (value < 0)
                {
                    throw new ArgumentOutOfRangeException("Invalid height");
                }

                this.height = value;
            }
        }

        public double Depth
        {
            get
            {
                return this.depth;
            }

            private set
            {
                if (value < 0)
                {
                    throw new ArgumentOutOfRangeException("Invalid depth");
                }

                this.depth = value;
            }
        }

        public double CalcVolume()
        {
            double volume = this.Width * this.Height * this.Depth;
            return volume;
        }

        public double CalcDiagonalXYZ()
        {
            double distance = PointUtils.CalcDistance3D(0, 0, 0, this.Width, this.Height, this.Depth);
            return distance;
        }

        public double CalcDiagonalXY()
        {
            double distance = PointUtils.CalcDistance2D(0, 0, this.Width, this.Height);
            return distance;
        }

        public double CalcDiagonalXZ()
        {
            double distance = PointUtils.CalcDistance2D(0, 0, this.Width, this.Depth);
            return distance;
        }

        public double CalcDiagonalYZ()
        {
            double distance = PointUtils.CalcDistance2D(0, 0, this.Height, this.Depth);
            return distance;
        }
    }
}
